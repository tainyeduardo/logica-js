//  de um triângulo (base e altura), calcular
//   e escrever a área do triângulo. Dica a=b.h/2

var base = parseInt(prompt("para calcular a área de um triangulo, primeiro insira sua base: "));
var altura = parseInt(prompt("agora, insira a altura do triangulo: "));
var area = (base*altura)/2;

alert("a area do triangulo é: "+area);
