// Escreva um algoritmo para ler as dimensões de um
//  losango (diagonal maior, diagonal menor), calcular
//   e escrever a área do losango. Dica a=D.d/2

var maior = parseInt(prompt("para calcular a área do losango, primeiro insira o valor da diagonal maior dele: "));
